"use client";

import type { NodePanelRendererContext } from "./nodePanelRenderers";
import type { UseNodePanelRenderContextArgs } from "./nodePanelRenderContextTypes";

export function buildNodePanelRenderContext({
  activePanel,
  activeSub,
  activeDetail,
  nodeId,
  asset,
  metadata,
  telemetryOverview,
  telemetryDetails,
  telemetryLoading,
  telemetryWindow,
  infraCategories,
  isDockerContainer,
  isDockerStack,
  isProxmoxAsset,
  effectiveKind,
  dockerHostForPanel,
  dockerContainerId,
  dockerStackHostId,
  dockerStackName,
  dockerStackAssetId,
  openPanel,
  openSystemDetail,
  closeSystemDetail,
  replaceDockerSub,
  telemetryWindowChange,
  logsLoading,
  logsError,
  logEvents,
  logLevelFilter,
  setLogLevelFilter,
  logMode,
  setLogMode,
  supportsLogQuery,
  logQueryModeLabel,
  logWindow,
  setLogWindow,
  journalSince,
  setJournalSince,
  journalUntil,
  setJournalUntil,
  journalUnit,
  setJournalUnit,
  journalPriority,
  setJournalPriority,
  journalQuery,
  setJournalQuery,
  journalLiveTail,
  setJournalLiveTail,
  refreshLogs,
  actionsLoading,
  actionRuns,
  expandedActionId,
  setExpandedActionId,
  proxmoxDetails,
  proxmoxLoading,
  proxmoxError,
  proxmoxNode,
  proxmoxVMID,
  canRunProxmoxQuickActions,
  proxmoxActionRunning,
  clusterStatus,
  networkInterfaces,
  proxmoxCollectorID,
  fetchProxmoxDetails,
  runProxmoxQuickAction,
  proxmoxCollectorId,
  proxmoxActionMessage,
  proxmoxActionError,
}: UseNodePanelRenderContextArgs): NodePanelRendererContext {
  return {
    activePanel,
    activeSub,
    activeDetail,
    nodeId,
    asset,
    metadata,
    telemetryOverview,
    telemetryDetails,
    telemetryLoading: telemetryLoading ?? false,
    telemetryWindow,
    infraCategories,
    isDockerContainer,
    isDockerStack,
    isProxmoxAsset,
    effectiveKind,
    dockerHostForPanel,
    dockerContainerId,
    dockerStackHostId,
    dockerStackName,
    dockerStackAssetId,
    openPanel,
    openSystemDetail,
    closeSystemDetail,
    replaceDockerSub,
    nodeMetricsTabProps: {
      telemetryDetails: telemetryDetails ?? null,
      telemetryLoading: telemetryLoading ?? false,
      telemetryWindow: telemetryWindow ?? "24h",
      onTelemetryWindowChange: telemetryWindowChange,
    },
    nodeLogsTabCardProps: {
      logsLoading,
      logsError,
      logEvents,
      logLevelFilter,
      onLogLevelFilterChange: setLogLevelFilter,
      logMode,
      onLogModeChange: setLogMode,
      supportsLogQuery,
      logQueryModeLabel,
      logWindow,
      onLogWindowChange: setLogWindow,
      journalSince,
      onJournalSinceChange: setJournalSince,
      journalUntil,
      onJournalUntilChange: setJournalUntil,
      journalUnit,
      onJournalUnitChange: setJournalUnit,
      journalPriority,
      onJournalPriorityChange: setJournalPriority,
      journalQuery,
      onJournalQueryChange: setJournalQuery,
      journalLiveTail,
      onJournalLiveTailChange: setJournalLiveTail,
      onRefresh: refreshLogs,
    },
    nodeActionsTabCardProps: {
      actionsLoading,
      actionRuns,
      expandedActionId,
      onExpandedActionChange: setExpandedActionId,
    },
    proxmoxDetailsTabProps: {
      proxmoxDetails,
      proxmoxLoading,
      proxmoxError,
      proxmoxNode,
      proxmoxVMID,
      effectiveKind,
      canRunProxmoxQuickActions,
      proxmoxActionRunning,
      clusterStatus,
      networkInterfaces,
      proxmoxCollectorID,
      nodeId,
      onRetry: () => {
        void fetchProxmoxDetails();
      },
      onRunProxmoxQuickAction: (actionID, params) => {
        void runProxmoxQuickAction(actionID, params);
      },
    },
    settingsTabProps: {
      collectorId: proxmoxCollectorId,
      loading: proxmoxLoading,
    },
    proxmoxActionMessage,
    proxmoxActionError,
    onRunStorageProxmoxAction: (actionID, target, params) => (
      runProxmoxQuickAction(actionID, params, target)
    ),
  };
}
